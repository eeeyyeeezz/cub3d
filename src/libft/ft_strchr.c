#include "libft.h"

int		ft_strchr(char *line, char c)
{
	int		i;

	i = 0;
	while (!(line[i] == c && line[i + 1] == ' ') && line[i] != '\0')
	{
		if (line[i] != ' ')
			return (0);
		i++;
	}
	return (1);
}
