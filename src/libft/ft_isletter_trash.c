#include "libft.h"

int	ft_isletter_trash(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') ||
	(c >= 33 && c <= 47) || (c >= 58 && c <= 126))
		return (1);
	return (0);
}
